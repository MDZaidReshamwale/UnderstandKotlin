open class Variables {
          fun varfun() {

//    VARIABLES
//    There are two types of variables in Kotlin: 1.var 2.val
//    var is mutable variable (can be changed) and val is immutable variable (can't be changed)

                var a = 10
                a = 20
                val b = 20
                //    b = 30 // error: val cannot be reassigned
//     print a and b value in single line
                println(" $a and  $b")

            }

    fun myfullName(){
